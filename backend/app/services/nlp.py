def normalize_product_name(name: str) -> str:
    """Приводит название продукта к начальной форме (лемме)."""
    words = name.lower().split()
    lemmas = []
    for word in words:
        parsed = morph.parse(word)[0]
        lemmas.append(parsed.normal_form)
    return " ".join(lemmas)

def get_emoji_for_product(lemma: str) -> str:
    """Подбирает эмодзи на основе лемматизированного названия."""
    words = lemma.split()
    # Ищем с конца, так как главное слово часто в конце (например "зеленое яблоко")
    for word in reversed(words):
        if word in EMOJI_DICT:
            return EMOJI_DICT[word]
    return "📦" # Дефолтный эмодзи
